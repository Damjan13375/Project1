$(document).ready(function() {
    $(window).scroll(function() {
        let scroll = $(window).scrollTop();
        if (scroll > 0) {
            $("#navbar").css("background", "rgba(0,0,0,0.7)");
        } else {
            $("#navbar").css("background", "");

        }
    });
});

